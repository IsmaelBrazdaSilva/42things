int	ft_str_is_alpha(char *str)
{
	int	i;
	int	f;

	f = 1;
	i = 0;
	while (str[i] != '\0')
	{
		if (str[i] >= 65 && str[i] <= 90)
		{
			f = 1;
		}
		else if (str[i] >= 97 && str[i] <= 122)
		{
			f = 1;
		}
		else
		{
			return (0);
		}
		i++;
	}
	return (f);
}
